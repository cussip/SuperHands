/* 함수의 종류
문자열 함수
숫자 함수
날짜 함수
변환 함수
NULL 관련 함수
집계 함수
*/

-----------------------------------------------------------------------------
-- 절대값을 구하는 함수 ABS(n)
-----------------------------------------------------------------------------
SELECT abs(35), abs(-35) FROM dual;

-----------------------------------------------------------------------------
-- 음수/양수를 알려주는 함수 SIGN(n)
-----------------------------------------------------------------------------
SELECT sign(35), sign(-35), sign(0) FROM dual;

-----------------------------------------------------------------------------
-- 반올림 함수 ROUND(n, i)
-----------------------------------------------------------------------------
SELECT round(34.4568), round(34.76788) FROM dual;
    -- 소수점 반올림
SELECT round(34.45678, 2), round(4335.678678, -3) FROM dual;
    -- 지정 자리수에서 반올림(음수 가능)

-----------------------------------------------------------------------------
-- 나머지 몫을 반환하는 함수 TRUNC(n1)
-- 나머지 값을 반환하는 함수 MOD(n1, n2)
-----------------------------------------------------------------------------
SELECT trunc(17/5) 몫, mod(17,5) 나머지 FROM dual;

-----------------------------------------------------------------------------
-- 제곱을 구하는 함수 POWER(n1, n2)
-- 제곱근을 구하는 함수 SQRT(n)
-----------------------------------------------------------------------------
SELECT power(3, 4), sqrt(25) FROM dual;

-----------------------------------------------------------------------------
-- 소수점 올림 CEIL(n)
-- 소수점 버림 FLOOR(n)
-----------------------------------------------------------------------------
SELECT ceil(34.333), floor(34.333) FROM dual;


