public class ReturnExam02 {

	//-----------------------------------------------------------------------------------------------------------
	// int plus(int num1, int num2)
	//-----------------------------------------------------------------------------------------------------------
	int plus(int num1, int num2) {
		return num1 + num2;
	} // End - int plus(int num1, int num2)
	
	//-----------------------------------------------------------------------------------------------------------
	// int minus(int num1, int num2)
	//-----------------------------------------------------------------------------------------------------------
	int minus(int num1, int num2) {
		return num1 - num2;
	}
	
	//-----------------------------------------------------------------------------------------------------------
	// public static void main(String[] args)
	//-----------------------------------------------------------------------------------------------------------
	public static void main(String[] args) {
		// 10 + 3의 결과와 10 -3의 결과를 plus(), minus() 메서드를 이용해서 보여주세요.
		ReturnExam02 re = new ReturnExam02();
		int result = re.plus(10, 3);
		System.out.println("더하기 결과 : " + result);
		
		System.out.println("빼기의 결과 : " + re.minus(10, 3));

	} // End - public static void main(String[] args)

} // End - public class ReturnExam02













